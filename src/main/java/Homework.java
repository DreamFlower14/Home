public class Homework {
    public static void main(String[] args) {
        int a,b = 5, c = 3;

        a = b + c;
        System.out.printf(" %d + %d = %d\n", b, c, a); // 더하기

        a = b - c;
        System.out.printf(" %d - %d = %d\n", b, c, a); // 빼기

        a = b * c;
        System.out.printf(" %d * %d = %d\n", b, c, a); // 곱하기

        a = b / c;
        System.out.printf(" %d / %d = %d\n", b, c, a); // 나누기

        a = b % c;
        System.out.printf(" %d %% %d = %d\n", b, c, a); // 나머지 값을 대입

    }
}
